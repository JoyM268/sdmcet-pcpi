;Program to add two 128-bit numbers
	AREA add128, CODE, READONLY
	LDR R0, =L1NUM1
	LDR R1, =L2NUM1
	LDR R2, =L1NUM2
	LDR R3, =L2NUM2
	LDR R10, =L1RESULT
	LDR R11, =L2RESULT
	LDR R12, =U1RESULT
	LDR R13, =U2RESULT
	
	LDR R4, [R0], #8
	LDR R5, [R1], #8
	LDR R6, [R2], #8
	LDR R7, [R3], #8
	
	ADDS R8, R4, R6
	ADCS R9, R5, R7
	STR R8, [R10]
	STR R9, [R11]
	
	LDR R4, [R0]
	LDR R5, [R1]
	LDR R6, [R2]
	LDR R7, [R3]
	
	ADCS R8, R4, R6	
	ADC R9, R5, R7
	
	STR R8, [R12]
	STR R9, [R13]
	NOP
	NOP
	AREA data1, DATA, READWRITE
L1RESULT DCD 0x0
L2RESULT DCD 0x0
U1RESULT DCD 0x0
U2RESULT DCD 0x0
L1NUM1 DCD 0x0
L2NUM1 DCD 0x0
U1NUM1 DCD 0x0
U2NUM1 DCD 0x0
L1NUM2 DCD 0x0
L2NUM2 DCD 0x0
U1NUM2 DCD 0x0
U2NUM2 DCD 0x0
	END